public class Math {

	int add (int one, int two) {

		int result = one + two; 

		return result; 
		
	}

	int sub (int one, int two) {

		int result = one - two; 

		return result; 
		
	}

	int mul (int one, int two) {

		int result = one * two; 

		return result; 
		
	}

	int div (int one, int two) {

		int result = one/two; 

		return result; 
		
	}
	
}
